public class Lab004_Multiplicationtable9 {
    public static void main(String[] args) {
        int num=9;
        int a;
        for(int i=0;i<=num;i++){
            a=9*i;
            System.out.printf("9*%d=%d", i,a);
            System.out.println();
        }
    }

}
